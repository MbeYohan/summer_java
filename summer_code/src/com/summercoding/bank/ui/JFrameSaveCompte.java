    private void champEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_champEnregistrerActionPerformed
        try {
            // TODO add your handling code here:
            
            String soldeString = champSolde.getText();
            
            double solde = Double.parseDouble(soldeString);
            
            String idAdmin = ComboBoxIdadmin.getSelectedItem().toString().split("")[0];
            int idadmin = Integer.parseInt(idAdmin);
            
            String idUser = ComboBoxIduser.getSelectedItem().toString().split("")[0];
            int iduser = Integer.parseInt(idUser);
            
            controleur.routeVersSaveCompte(solde, iduser, idadmin);
            
            JOptionPane.showMessageDialog(null, "succ√®s");
            
            champSolde.setText("");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JFrameSaveCompte.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(JFrameSaveCompte.class.getName()).log(Level.SEVERE, null, ex);
        }
         
        
         
        
        
    }//GEN-LAST:event_champEnregistrerActionPerformed

